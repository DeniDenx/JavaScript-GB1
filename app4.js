'use strict';

function getResult1(a, b) {
    return a + b;
}

function getResult2(a, b) {
    return a - b;
}

function getResult3(a, b) {
    return a * b;
}

function getResult4(a, b) {
    return a / b;
}

console.log(getResult1(23, 6));
console.log(getResult2(23, 6));
console.log(getResult3(23, 6));
console.log(getResult4(23, 6));
